def select(name):
   na = name[0]
   ro = int(name[1])
   re = int(name[2])
   ans = c[na]
   for i in range(ro):
      ans = rotate(ans)
   for i in range(re):
      ans = reflect(ans)
   return ans

def rotate(m): 
   return [[m[len(m) - j - 1][i] for j in range(len(m))] for i in range(len(m[0]))]
   
def reflect(m):
   return m[::-1]